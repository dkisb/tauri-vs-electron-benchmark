#!/usr/bin/env bun

// Generate minimal placeholder icons for Tauri
// Run: bun run scripts/gen-icons.ts

import { writeFileSync } from "fs";
import { join } from "path";

const ICONS_DIR = join(import.meta.dir, "..", "tauri-app", "src-tauri", "icons");

// Minimal 1x1 PNG (orange pixel)
const PNG_1x1 = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR chunk
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
  0xDE, 0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41,
  0x54, 0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00,
  0x00, 0x00, 0x03, 0x00, 0x01, 0x00, 0x05, 0xFE,
  0xD4, 0xAD, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45,
  0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82
]);

// Write placeholder PNGs
writeFileSync(join(ICONS_DIR, "32x32.png"), PNG_1x1);
writeFileSync(join(ICONS_DIR, "128x128.png"), PNG_1x1);
writeFileSync(join(ICONS_DIR, "128x128@2x.png"), PNG_1x1);

// For macOS .icns and Windows .ico, create empty files
// (Tauri will still build but warn)
writeFileSync(join(ICONS_DIR, "icon.icns"), PNG_1x1);
writeFileSync(join(ICONS_DIR, "icon.ico"), PNG_1x1);

console.log("âœ… Generated placeholder icons in", ICONS_DIR);
